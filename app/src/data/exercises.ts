export const getExerciseById = (id: string): Exercise | undefined => {
  return exercises.find(e => e.id === id);
};

export const getExercisesByCategory = (category: string): Exercise[] => {
  return exercises.filter(e => e.category === category);
};

export const getExercisesByEquipment = (equipmentId: string): Exercise[] => {
  return exercises.filter(e => e.equipment.some(eq => eq.id === equipmentId));
};

export const searchExercises = (query: string): Exercise[] => {
  const lowerQuery = query.toLowerCase();
  return exercises.filter(e =>
    e.name.toLowerCase().includes(lowerQuery) ||
    e.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    e.category.toLowerCase().includes(lowerQuery)
  );
};

export const getCategories = (): string[] => {
  return [...new Set(exercises.map(e => e.category))];
};
